public class Risk {
  private int pro;
  private int con;
  private int sev;

  void setPro (int pro) {
    this.pro = pro;
  }
  void setCon (int con) {
    this.con = con;
  }
  void setSev (int sev) {
    this.sev = sev;
  }

  int getPro() {
    return pro;
  }
  int getCon() {
    return con;
  }
  int getSev() {
    return sev;
  }
}
